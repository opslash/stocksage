// =============================================================================
// pdf_export.js — Client-Side PDF Report Generator
// StockSage · Feature: One-click PDF via html2pdf.js
// =============================================================================

window.exportPDF = async function () {
    const stock = window.currentStockData || (window.AppState ? window.AppState.stockData : null);
    if (!stock) {
        if (window.showToast) showToast('Load stock data before exporting.', 'error');
        return;
    }

    const btn = document.querySelector('button:contains("Export PDF")') || document.getElementById('export-pdf-btn') || document.getElementById('export-btn');
    if (btn) btn.innerText = 'Generating...';

    // 1. Create a clean, off-screen container
    const container = document.createElement('div');
    container.style.cssText = 'width: 800px; padding: 40px; background: #ffffff; color: #0f172a; font-family: sans-serif; position: absolute; left: -9999px;';

    // 2. Populate with clean HTML layout
    container.innerHTML = `
      <div style="border-bottom: 2px solid #e2e8f0; padding-bottom: 16px; margin-bottom: 24px;">
        <h1 style="margin: 0; font-size: 28px;">${stock.name || stock.company_name || 'Stock'} (${stock.ticker || stock.symbol || 'N/A'})</h1>
        <p style="margin: 8px 0 0; color: #475569; font-size: 18px;">Price: $${stock.price || stock.current_price || 'N/A'} | Market Cap: $${fmt(stock.marketCap || stock.market_cap, 'currency').replace('$', '')}</p>
      </div>
      
      <h3 style="color: #334155; margin-bottom: 12px;">Key Financial Metrics</h3>
      <table style="width: 100%; border-collapse: collapse; text-align: left;">
        <tr style="border-bottom: 1px solid #e2e8f0;">
          <td style="padding: 10px 0;">P/E (TTM)</td><td style="font-weight: bold;">${stock.pe_ttm || 'N/A'}x</td>
          <td style="padding: 10px 0;">P/FCF (TTM)</td><td style="font-weight: bold;">${stock.pfcf_ttm || 'N/A'}x</td>
        </tr>
        <tr style="border-bottom: 1px solid #e2e8f0;">
          <td style="padding: 10px 0;">Profit Margin</td><td style="font-weight: bold;">${stock.niMargin_ttm || stock.profit_margin ? ((stock.niMargin_ttm || stock.profit_margin) * 100).toFixed(2) + '%' : 'N/A'}</td>
          <td style="padding: 10px 0;">ROIC (5-Yr)</td><td style="font-weight: bold;">${stock.avg_roic_5yr || stock.roic_5yr ? ((stock.avg_roic_5yr || stock.roic_5yr) * 100).toFixed(1) + '%' : 'N/A'}</td>
        </tr>
        <tr style="border-bottom: 1px solid #e2e8f0;">
          <td style="padding: 10px 0;">Revenue (TTM)</td><td style="font-weight: bold;">$${fmt(stock.revenue_ttm, 'currency').replace('$', '') || 'N/A'}</td>
          <td style="padding: 10px 0;">Free Cash Flow</td><td style="font-weight: bold;">$${fmt(stock.fcf_ttm, 'currency').replace('$', '') || 'N/A'}</td>
        </tr>
      </table>
      <div style="margin-top: 40px; font-size: 12px; color: #94a3b8; text-align: center;">Generated by StockSage Intelligence</div>
    `;

    document.body.appendChild(container);

    // 3. Export configuration
    const options = {
        margin: 0.5,
        filename: `${stock.ticker || stock.symbol || 'Stock'}_StockSage_Report.pdf`,
        image: { type: 'jpeg', quality: 0.98 },
        html2canvas: { scale: 2, useCORS: true, logging: false },
        jsPDF: { unit: 'in', format: 'letter', orientation: 'portrait' }
    };

    // 4. Generate and cleanup
    try {
        await html2pdf().set(options).from(container).save();
    } finally {
        document.body.removeChild(container);
        if (btn) btn.innerText = '📄 Export PDF';
    }
};
